struct DSU{
	vector<int> par, weight;
	int components, nodes;

	DSU(int n){
		nodes = n;
		components = n;
		par.resize(n+1);
		weight.resize(n+1, 1);
		iota(par.begin(), par.end(), 0);
	}

	int Find(int v){ // find set with path compression
		if(v==par[v])
			return v;
		return par[v] = Find(par[v]);
	}

	bool Union(int a, int b){
		a = Find(a), b = Find(b);
		if(a == b) return false;
		if(weight[a] > weight[b]){
			swap(a,b);
		}
		par[a] = b;
		weight[b] += weight[a];
		components -= 1;
		return true;
	}

	int CntofComponent(){ // return the total number of components
		return components;
	}

	int NodeinComponent(int x){ // return the number of nodes in component x;
		return weight[Find(x)];
	}
};


void solve(){
	int n;
	cin >> n;
	
	vector<tuple<int,int,int>> edges;
	for(int i=0; i<n-1; i++){
		int u, v, w;
		cin >> u >> v >> w;
		edges.push_back({w,u,v});
	}
	sort(begin(edges), end(edges));
	
	
	DSU dsu(n);
	int ans = 0;
	for(auto &[w, u, v]: edges){
		int c1 = dsu.NodeinComponent(u), c2 = dsu.NodeinComponent(v);
		ans += (w * c1 * c2);
		dsu.Union(u, v);
	}
	
	cout << ans << endl;
	
}

